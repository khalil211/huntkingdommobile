package com.cybersquad.huntkingdom.entities.produit;

import java.util.Objects;

/**
 *
 * @author user
 */
public class CategorieProduit {
    private int id;
    private String nom;
    private String description;

    public CategorieProduit(int id, String nom, String description) {
        this.id = id;
        this.nom = nom;
        this.description = description;
    }

    public CategorieProduit(String nom, String description) {
        this.nom = nom;
        this.description = description;
    }

    public CategorieProduit() {
    }

    public int getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public String getDescription() {
        return description;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return nom;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 59 * hash + this.id;
        hash = 59 * hash + Objects.hashCode(this.nom);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final CategorieProduit other = (CategorieProduit) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.nom, other.nom)) {
            return false;
        }
        return true;
    }
}
